/* 1. 环境映射 */
// 我们现在将整个环境映射到了一个纹理对象上了, 能利用这个信息的不仅仅只有天空盒
// 通过使用环境的立方体贴图, 我们可以给物体反射和折射的属性
// 这样使用环境立方体贴图的技术叫做环境映射(Environment Mapping)
// 其中最流行的两个是反射(Reflection)和折射(Refraction)

/* 2. 反射 */
// 反射这个属性表现为物体(或物体的一部分)反射它周围环境
// 即根据观察者的视角, 物体的颜色或多或少等于它的环境
// 镜子就是一个反射性物体: 它会根据观察者的视角反射它周围的环境

// 当反射应用到一整个物体上(像是箱子)时, 这个物体看起来就像是钢或者铬这样的高反射性材质

/* 3. 折射 */
// 你可以想象出有了光照, 反射, 折射和顶点移动的正确组合, 你可以创建出非常漂亮的水
// 注意, 如果要想获得物理上精确的结果, 我们还需要在光线离开物体的时候再次折射
// 现在我们使用的只是单面折射(Single-side Refraction), 但它对大部分场合都是没问题的

/* 4. 动态环境贴图 */
// 利用帧缓冲先渲染出不同方位的环境贴图

// 现在我们使用的都是静态图像的组合来作为天空盒, 看起来很不错, 但它没有在场景中包括可移动的物体
// 我们一直都没有注意到这一点, 因为我们只使用了一个物体
// 如果我们有一个镜子一样的物体, 周围还有多个物体, 镜子中可见的只有天空盒
// 看起来就像它是场景中唯一一个物体一样

// 通过使用帧缓冲, 我们能够为物体的 6 个不同角度创建出场景的纹理
// 并在每个渲染迭代中将它们储存到一个立方体贴图中
// 之后我们就可以使用这个(动态生成的)立方体贴图来创建出更真实的, 包含其它物体的, 反射和折射表面了
// 这就叫做动态环境映射(Dynamic Environment Mapping)
// 因为我们动态创建了物体周围的立方体贴图, 并将其用作环境贴图

// 虽然它看起来很棒, 但它有一个很大的缺点
// 我们需要为使用环境贴图的物体渲染场景 6 次, 这是对程序是非常大的性能开销
// 现代的程序通常会尽可能使用天空盒, 并在可能的时候使用预编译的立方体贴图
// 只要它们能产生一点动态环境贴图的效果
// 虽然动态环境贴图是一个很棒的技术, 但是要想在不降低性能的情况下让它工作还是需要非常多的技巧的

