/* 1. 立方体贴图 cube maps */
// 立方体贴图有一个非常有用的特性
// 它可以通过一个方向向量来进行索引/采样
// 假设我们有一个1x1x1的单位立方体, 方向向量的原点位于它的中心

// 方向向量的大小并不重要, 只要提供了方向
// OpenGL 就会获取方向向量(最终)所击中的纹素, 并返回对应的采样纹理值

// 如果我们假设将这样的立方体贴图应用到一个立方体上
// 采样立方体贴图所使用的方向向量将和立方体(插值的)顶点位置非常相像
// 这样子, 只要立方体的中心位于原点, 我们就能使用立方体的实际位置向量来对立方体贴图进行采样
// 接下来, 我们可以将所有顶点的纹理坐标当做是立方体的顶点位置
// 最终得到的结果就是可以访问立方体贴图上正确面(Face)纹理的一个纹理坐标


/* 2. skybox 天空盒(不动的) */
// (1) 绘制天空盒的方法
// 将它变为场景中的第一个渲染的物体, 并且禁用深度写入
// 这样子天空盒就会永远被绘制在其它物体的背后了

// (2) 优化后的绘制方法
// [1] 问题
// 如果我们先渲染天空盒, 我们就会对屏幕上的每一个像素运行一遍片段着色器, 即便只有一小部分的天空盒最终是可见的
// 可以使用提前深度测试(Early Depth Testing)轻松丢弃掉的片段能够节省我们很多宝贵的带宽

// 我们将会最后渲染天空盒, 以获得轻微的性能提升
// 这样子的话, 深度缓冲就会填充满所有物体的深度值了
// 我们只需要在提前深度测试通过的地方渲染天空盒的片段就可以了
// 很大程度上减少了片段着色器的调用

// 问题是, 天空盒只是一个1x1x1的立方体, 它很可能会不通过大部分的深度测试, 导致渲染失败
// 不用深度测试来进行渲染不是解决方案, 因为天空盒将会复写场景中的其它物体
// 我们需要欺骗深度缓冲, 让它认为天空盒有着最大的深度值 1.0, 只要它前面有一个物体, 深度测试就会失败

// 要求将天空盒的深度设置为 1.0

// 在坐标系统小节中我们说过, 透视除法是在顶点着色器运行之后执行的
// 将 gl_Position 的 xyz 坐标除以 w 分量
// 我们又从深度测试小节中知道, 相除结果的z分量等于顶点的深度值
// 使用这些信息, 我们可以将输出位置的z分量等于它的 w 分量, 让 z 分量永远等于 1.0
// 这样子的话, 当透视除法执行之后, z 分量会变为 w/w = 1.0

// 我们还要改变一下深度函数, 将它从默认的 GL_LESS 改为 GL_LEQUAL
// 深度缓冲将会填充上天空盒的1.0值
// 所以我们需要保证天空盒在值小于或等于深度缓冲而不是小于时通过深度测试

